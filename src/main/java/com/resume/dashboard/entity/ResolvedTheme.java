package com.resume.dashboard.entity;

public class ResolvedTheme {

    private String primaryColor;
    private String accentColor;
    private String backgroundColor;
    private String textColor;
    private String fontFamily;
    private String borderRadius;
    private String motionLevel;

    public String getPrimaryColor() { return primaryColor; }
    public void setPrimaryColor(String primaryColor) { this.primaryColor = primaryColor; }
    public String getAccentColor() { return accentColor; }
    public void setAccentColor(String accentColor) { this.accentColor = accentColor; }
    public String getBackgroundColor() { return backgroundColor; }
    public void setBackgroundColor(String backgroundColor) { this.backgroundColor = backgroundColor; }
    public String getTextColor() { return textColor; }
    public void setTextColor(String textColor) { this.textColor = textColor; }
    public String getFontFamily() { return fontFamily; }
    public void setFontFamily(String fontFamily) { this.fontFamily = fontFamily; }
    public String getBorderRadius() { return borderRadius; }
    public void setBorderRadius(String borderRadius) { this.borderRadius = borderRadius; }
    public String getMotionLevel() { return motionLevel; }
    public void setMotionLevel(String motionLevel) { this.motionLevel = motionLevel; }
}
