package com.resume.dashboard.entity;

/**
 * The emotional/visual personality of a theme or layout.
 * Drives what kind of backgrounds, textures, and effects are appropriate.
 */
public enum VisualMood {
    CLEAN_MINIMAL,       // White space, light palette, no texture
    BOLD_VIBRANT,        // High contrast, vivid colors, strong type
    DARK_DRAMATIC,       // Dark backgrounds, glows, deep shadows
    EARTHY_ORGANIC,      // Muted tones, paper/grain textures, nature vibes
    LUXURY_ELEGANT,      // Gold/silver accents, editorial spacing, serif fonts
    PLAYFUL_QUIRKY,      // Bright pastels, rounded shapes, fun type
    CORPORATE_FORMAL,    // Navy/grey palette, structured, no flourishes
    RETRO_VINTAGE,       // Aged paper, worn textures, muted warm tones
    FUTURISTIC_TECH,     // Neon on dark, grid lines, geometric precision
    ARTISTIC_EXPRESSIVE  // Painterly textures, mixed media feel, abstract shapes
}